namespace Apex.WorldGeometry
{
    /// <summary>
    /// Represents the 3 axis in world space.
    /// </summary>
    public enum Axis
    {
        /// <summary>
        /// No axis - we must have entered the fourth dimension
        /// </summary>
        None,

        /// <summary>
        /// The x axis
        /// </summary>
        X,

        /// <summary>
        /// The y axis
        /// </summary>
        Y,

        /// <summary>
        /// The z axis
        /// </summary>
        Z
    }
}
